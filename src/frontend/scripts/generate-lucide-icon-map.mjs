/**
 * Extracts icon node data from lucide-react ESM source files and generates
 * a compact TypeScript map used by DataTable header icons.
 *
 * Usage: node scripts/generate-lucide-icon-map.mjs
 */

import { readdir, readFile, writeFile } from 'node:fs/promises';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ICONS_DIR = join(
  __dirname,
  '../node_modules/lucide-react/dist/esm/icons',
);
const OUTPUT_FILE = join(
  __dirname,
  '../src/widgets/dataTables/utils/lucideIconNodes.generated.ts',
);

// Matches: const __iconNode = [ ... ];
const ICON_NODE_RE = /const __iconNode\s*=\s*(\[[\s\S]*?\]);\s*\n/;
// Matches: const PascalName = createLucideIcon("kebab-name", __iconNode);
const EXPORT_NAME_RE = /const (\w+)\s*=\s*createLucideIcon\(/;

/**
 * Parse a JS object literal subset (arrays, objects with string values) that
 * may use unquoted keys. This is NOT a general-purpose parser — it handles
 * exactly the format lucide-react emits for __iconNode arrays.
 */
function parseIconNode(raw) {
  // Normalize: wrap unquoted object keys in quotes, remove trailing commas
  let json = raw
    // key: "value"  →  "key": "value"   (unquoted identifier keys)
    .replace(/(\{|,)\s*([a-zA-Z_]\w*)\s*:/g, '$1 "$2":')
    // Remove trailing commas before } or ]
    .replace(/,\s*([}\]])/g, '$1');

  try {
    const nodes = JSON.parse(json);
    // Strip 'key' properties — they're React reconciliation keys, not SVG attrs
    return nodes.map(([tag, attrs]) => {
      const { key, ...rest } = attrs;
      return [tag, rest];
    });
  } catch {
    return null;
  }
}

async function main() {
  const files = (await readdir(ICONS_DIR)).filter(
    f => f.endsWith('.js') && !f.endsWith('.map'),
  );

  const icons = {};
  let skipped = 0;

  for (const file of files) {
    const src = await readFile(join(ICONS_DIR, file), 'utf-8');

    const nameMatch = src.match(EXPORT_NAME_RE);
    const nodeMatch = src.match(ICON_NODE_RE);

    if (!nameMatch || !nodeMatch) {
      skipped++;
      continue;
    }

    const pascalName = nameMatch[1];
    const nodes = parseIconNode(nodeMatch[1]);

    if (!nodes) {
      skipped++;
      continue;
    }

    icons[pascalName] = nodes;
  }

  // Generate compact TypeScript output
  const entries = Object.keys(icons)
    .sort()
    .map(name => `  ${JSON.stringify(name)}: ${JSON.stringify(icons[name])}`)
    .join(',\n');

  const output = `// Auto-generated by scripts/generate-lucide-icon-map.mjs — DO NOT EDIT
// Source: lucide-react v0.562.0
// Icons: ${Object.keys(icons).length} | Skipped: ${skipped}

export type IconNode = [string, Record<string, string>];

export const LUCIDE_ICONS: Record<string, IconNode[]> = {
${entries}
};
`;

  await writeFile(OUTPUT_FILE, output, 'utf-8');
  console.log(
    `Generated ${Object.keys(icons).length} icons (${skipped} skipped) → ${OUTPUT_FILE}`,
  );

  // Report file size
  const stat = await readFile(OUTPUT_FILE);
  console.log(`File size: ${(stat.length / 1024).toFixed(1)} KB`);
}

main().catch(err => {
  console.error(err);
  process.exit(1);
});
