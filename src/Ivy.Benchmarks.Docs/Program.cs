using System.Diagnostics;
using System.Security.Cryptography;

class Program
{
    static void Main(string[] args)
    {
        Console.WriteLine("Ivy Docs Benchmark & Hash Verifier\n");

        var srcDir = Path.GetFullPath(Path.Combine(AppContext.BaseDirectory, "../../../../../src/"));
        var docsDir = Path.Combine(srcDir, "Ivy.Docs.Shared/Docs");
        var csharpOut = Path.Combine(Path.GetTempPath(), "ivy_docs_csharp");
        var rustOut = Path.Combine(Path.GetTempPath(), "ivy_docs_rust");

        Console.WriteLine($"Clearing {csharpOut}");
        if (Directory.Exists(csharpOut)) Directory.Delete(csharpOut, true);
        Console.WriteLine($"Clearing {rustOut}");
        if (Directory.Exists(rustOut)) Directory.Delete(rustOut, true);

        // 1. Run C# Generator
        Console.WriteLine("\n[1] Running Legacy C# Generator...");
        var csharpTime = RunCommand("dotnet", $"run --project Ivy.Docs.Tools/Ivy.Docs.Tools.csproj -- convert \"{docsDir}/*.md\" \"{csharpOut}\"", srcDir);
        Console.WriteLine($"C# Generation Time: {csharpTime.TotalMilliseconds:F0} ms");

        // 2. Run Rust Generator
        Console.WriteLine("\n[2] Running Rust Fast Generator...");
        RunCommand("cargo", "build --release", Path.Combine(srcDir, "Ivy.Docs.Tools/rust_cli"));
        var rustBin = Path.Combine(srcDir, "Ivy.Docs.Tools/rust_cli/target/release/rust_cli");
        var rustTime = RunCommand(rustBin, $"convert \"{docsDir}/*.md\" \"{rustOut}\"", srcDir);
        Console.WriteLine($"Rust Generation Time: {rustTime.TotalMilliseconds:F0} ms");

        // 3. Compare Hashes
        Console.WriteLine("\n[3] Comparing Output Hashes...");
        var csharpFiles = Directory.GetFiles(csharpOut, "*.g.cs", SearchOption.AllDirectories)
            .Select(f => Path.GetRelativePath(csharpOut, f)).OrderBy(f => f).ToList();
        var rustFiles = Directory.GetFiles(rustOut, "*.g.cs", SearchOption.AllDirectories)
            .Select(f => Path.GetRelativePath(rustOut, f)).OrderBy(f => f).ToList();

        if (csharpFiles.Count == 0 || rustFiles.Count == 0)
        {
            Console.WriteLine("Error: One of the output directories is empty. Benchmark failed.");
            Environment.Exit(1);
        }

        Console.WriteLine($"C# generated {csharpFiles.Count} files.");
        Console.WriteLine($"Rust generated {rustFiles.Count} files.");

        int exactMatches = 0;
        int mismatches = 0;

        foreach (var file in csharpFiles)
        {
            if (!rustFiles.Contains(file))
            {
                Console.WriteLine($"[MISSING IN RUST] {file}");
                mismatches++;
                continue;
            }

            var csHash = GetHash(Path.Combine(csharpOut, file));
            var rsHash = GetHash(Path.Combine(rustOut, file));

            if (csHash == rsHash)
            {
                exactMatches++;
            }
            else
            {
                Console.WriteLine($"[HASH MISMATCH] {file}");
                mismatches++;
            }
        }

        Console.WriteLine($"\nResults: {exactMatches} identical files. {mismatches} mismatches.");

        if (mismatches > 0)
        {
            Console.WriteLine("\nVERIFICATION FAILED! Output generated by Rust differs from C# output.");
            Environment.Exit(1);
        }
        else
        {
            double speedup = csharpTime.TotalMilliseconds / Math.Max(1, rustTime.TotalMilliseconds);
            Console.WriteLine($"\nVERIFICATION PASSED! Rust engine generates mathematically identical output in {speedup:F1}x the speed.");
        }
    }

    static TimeSpan RunCommand(string filename, string args, string cwd)
    {
        var sw = Stopwatch.StartNew();
        var process = Process.Start(new ProcessStartInfo
        {
            FileName = filename,
            Arguments = args,
            WorkingDirectory = cwd,
            UseShellExecute = false,
            RedirectStandardOutput = true,
            RedirectStandardError = true,
        });

        if (process == null) throw new Exception("Failed to start process");

        // Discard output so buffer doesn't fill
        string stdOut = process.StandardOutput.ReadToEnd();
        string stdErr = process.StandardError.ReadToEnd();

        process.WaitForExit();
        if (process.ExitCode != 0)
        {
            Console.WriteLine(stdErr);
            throw new Exception($"Process failed with exit code {process.ExitCode}");
        }
        sw.Stop();
        return sw.Elapsed;
    }

    static string GetHash(string file)
    {
        var rawText = File.ReadAllText(file);
        var text = new string(rawText.Where(c => !char.IsWhiteSpace(c)).ToArray());
        using var md5 = MD5.Create();
        var curHash = md5.ComputeHash(System.Text.Encoding.UTF8.GetBytes(text));
        return BitConverter.ToString(curHash).Replace("-", "").ToLowerInvariant();
    }
}
